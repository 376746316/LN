package com.chinasofti.cetp.study.model;

import java.util.Date;

public class CourseNote {
	private Integer specId;
	private String specName;
	private String specThumb;
	private Integer noteNum;
	private Date lastEdit;
	private Integer scNum;

	public Integer getSpecId() {
		return specId;
	}

	public void setSpecId(Integer specId) {
		this.specId = specId;
	}

	public String getSpecName() {
		return specName;
	}

	public void setSpecName(String specName) {
		this.specName = specName;
	}

	public String getSpecThumb() {
		return specThumb;
	}

	public void setSpecThumb(String specThumb) {
		this.specThumb = specThumb;
	}

	public Integer getNoteNum() {
		return noteNum;
	}

	public void setNoteNum(Integer noteNum) {
		this.noteNum = noteNum;
	}

	public Date getLastEdit() {
		return lastEdit;
	}

	public void setLastEdit(Date lastEdit) {
		this.lastEdit = lastEdit;
	}

	public Integer getScNum() {
		return scNum;
	}

	public void setScNum(Integer scNum) {
		this.scNum = scNum;
	}

}
