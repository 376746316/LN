package com.chinasofti.cetp.study.model;

import java.util.List;

public class MoreTechType extends TechType {

	private int id;
	private String text;
	private List<MoreTechType> children;
	private String state;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	public List<MoreTechType> getChildren() {
		return children;
	}

	public void setChildren(List<MoreTechType> children) {
		this.children = children;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

}
