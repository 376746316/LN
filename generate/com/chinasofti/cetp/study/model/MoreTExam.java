package com.chinasofti.cetp.study.model;

public class MoreTExam extends TExam {
	private Integer epFullscore;
	private Integer euId;
	private Long euStatus;
	private Integer chapId;
	private String chapName;

	public Integer getChapId() {
		return chapId;
	}

	public void setChapId(Integer chapId) {
		this.chapId = chapId;
	}

	public String getChapName() {
		return chapName;
	}

	public void setChapName(String chapName) {
		this.chapName = chapName;
	}

	public Integer getEpFullscore() {
		return epFullscore;
	}

	public void setEpFullscore(Integer epFullscore) {
		this.epFullscore = epFullscore;
	}

	public Integer getEuId() {
		return euId;
	}

	public void setEuId(Integer euId) {
		this.euId = euId;
	}

	public Long getEuStatus() {
		return euStatus;
	}

	public void setEuStatus(Long euStatus) {
		this.euStatus = euStatus;
	}

}
