package com.chinasofti.cetp.study.model;

import java.util.List;

public class MoreStudyQuestion extends StudyQuestion {
	private String stuName;
	private MoreStudyAnswer studyAnswer;
	private int count;
	private String simpleDate;
	private List<MoreStudyAnswer> otherAnswerList;

	public List<MoreStudyAnswer> getOtherAnswerList() {
		return otherAnswerList;
	}

	public void setOtherAnswerList(List<MoreStudyAnswer> otherAnswerList) {
		this.otherAnswerList = otherAnswerList;
	}

	public String getSimpleDate() {
		return simpleDate;
	}

	public void setSimpleDate(String simpleDate) {
		this.simpleDate = simpleDate;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

	public MoreStudyAnswer getStudyAnswer() {
		return studyAnswer;
	}

	public void setStudyAnswer(MoreStudyAnswer studyAnswer) {
		this.studyAnswer = studyAnswer;
	}

	public String getStuName() {
		return stuName;
	}

	public void setStuName(String stuName) {
		this.stuName = stuName;
	}
}
