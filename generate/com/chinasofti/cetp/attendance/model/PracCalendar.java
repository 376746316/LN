package com.chinasofti.cetp.attendance.model;

import java.util.Date;

public class PracCalendar {
    private Integer pacId;

    private Integer pacPracId;

    private Date pacDate;

    private Integer pacAsId;

    private Integer pacUserNo;

    private Date pacRegtime;

    public Integer getPacId() {
        return pacId;
    }

    public void setPacId(Integer pacId) {
        this.pacId = pacId;
    }

    public Integer getPacPracId() {
        return pacPracId;
    }

    public void setPacPracId(Integer pacPracId) {
        this.pacPracId = pacPracId;
    }

    public Date getPacDate() {
        return pacDate;
    }

    public void setPacDate(Date pacDate) {
        this.pacDate = pacDate;
    }

    public Integer getPacAsId() {
        return pacAsId;
    }

    public void setPacAsId(Integer pacAsId) {
        this.pacAsId = pacAsId;
    }

    public Integer getPacUserNo() {
        return pacUserNo;
    }

    public void setPacUserNo(Integer pacUserNo) {
        this.pacUserNo = pacUserNo;
    }

    public Date getPacRegtime() {
        return pacRegtime;
    }

    public void setPacRegtime(Date pacRegtime) {
        this.pacRegtime = pacRegtime;
    }
}