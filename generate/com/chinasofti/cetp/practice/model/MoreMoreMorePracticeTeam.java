package com.chinasofti.cetp.practice.model;

public class MoreMoreMorePracticeTeam extends PracticeTeam {
	private String pracName;
	private String proName;
	private String teamSummary;
	public String getPracName() {
		return pracName;
	}
	public void setPracName(String pracName) {
		this.pracName = pracName;
	}
	public String getProName() {
		return proName;
	}
	public void setProName(String proName) {
		this.proName = proName;
	}
	public String getTeamSummary() {
		return teamSummary;
	}
	public void setTeamSummary(String teamSummary) {
		this.teamSummary = teamSummary;
	}
}
