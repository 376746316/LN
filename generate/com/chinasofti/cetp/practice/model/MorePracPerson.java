package com.chinasofti.cetp.practice.model;

import java.util.Date;

public class MorePracPerson extends PracPersonKey {
	private Double finPercent;
	private String proName;
	private String stuName;
	private String pracName;
	private String ppSummary;
	private String ppEvaluate;
	private Date ppRegtime;
	
	public Double getFinPercent() {
		return finPercent;
	}
	public void setFinPercent(Double finPercent) {
		this.finPercent = finPercent;
	}
	public String getProName() {
		return proName;
	}
	public void setProName(String proName) {
		this.proName = proName;
	}
	public String getStuName() {
		return stuName;
	}
	public void setStuName(String stuName) {
		this.stuName = stuName;
	}
	public String getPracName() {
		return pracName;
	}
	public void setPracName(String pracName) {
		this.pracName = pracName;
	}
	public String getPpSummary() {
		return ppSummary;
	}
	public void setPpSummary(String ppSummary) {
		this.ppSummary = ppSummary;
	}
	public String getPpEvaluate() {
		return ppEvaluate;
	}
	public void setPpEvaluate(String ppEvaluate) {
		this.ppEvaluate = ppEvaluate;
	}
	public Date getPpRegtime() {
		return ppRegtime;
	}
	public void setPpRegtime(Date ppRegtime) {
		this.ppRegtime = ppRegtime;
	}
	
}
