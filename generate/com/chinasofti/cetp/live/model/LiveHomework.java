package com.chinasofti.cetp.live.model;

import java.util.Date;

public class LiveHomework extends LiveHomeworkKey {
    private String lhwFlag;

    private Integer lhwUserNo;

    private Date lhwRegtime;

    public String getLhwFlag() {
        return lhwFlag;
    }

    public void setLhwFlag(String lhwFlag) {
        this.lhwFlag = lhwFlag == null ? null : lhwFlag.trim();
    }

    public Integer getLhwUserNo() {
        return lhwUserNo;
    }

    public void setLhwUserNo(Integer lhwUserNo) {
        this.lhwUserNo = lhwUserNo;
    }

    public Date getLhwRegtime() {
        return lhwRegtime;
    }

    public void setLhwRegtime(Date lhwRegtime) {
        this.lhwRegtime = lhwRegtime;
    }
}