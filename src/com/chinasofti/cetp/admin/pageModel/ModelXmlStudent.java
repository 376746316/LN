package com.chinasofti.cetp.admin.pageModel;

import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;

/**
 * 用户信息xml数据模型
 * @author Administrator
 *
 */
@XmlRootElement(name = "modelXmlStudent")
public class ModelXmlStudent {

	private String stuUserid;

	private String stuHead;

	private String stuHeadM;

	private String stuHeadS;

	public String getStuUserid() {
		return stuUserid;
	}

	@XmlElement
	public void setStuUserid(String stuUserid) {
		this.stuUserid = stuUserid;
	}

	public String getStuHead() {
		return stuHead;
	}

	@XmlElement
	public void setStuHead(String stuHead) {
		this.stuHead = stuHead;
	}

	public String getStuHeadM() {
		return stuHeadM;
	}

	@XmlElement
	public void setStuHeadM(String stuHeadM) {
		this.stuHeadM = stuHeadM;
	}

	public String getStuHeadS() {
		return stuHeadS;
	}

	@XmlElement
	public void setStuHeadS(String stuHeadS) {
		this.stuHeadS = stuHeadS;
	}

}
